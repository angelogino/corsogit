/* addizione */
function addizione (m1,m2){
  return m1+m2;
}

/* sottrazione */
function sottrazione (m1,m2){
  return m1-m2;
}

/* moltiplicazione */
function moltiplicazione (m1,m2){
  return m1*m2;
}

/* divisione */
function  divisione (m1,m2){
  if (m2!== 0;){
    return m1/m2;
  }
  else{
    return "error";
  }
}

